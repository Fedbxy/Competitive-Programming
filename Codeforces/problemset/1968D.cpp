#include <bits/stdc++.h>

using namespace std;

void solve() {
    int n, k, pb, ps; cin >> n >> k >> pb >> ps;
    vector<int> step(n), pt(n);
    for(auto &e: step) cin >> e, --e;
    for(auto &e: pt) cin >> e;
    --pb;
    --ps;

    
}

int main() {
    cin.tie(nullptr)->sync_with_stdio(false);
    int t; cin >> t;
    while(t--) solve();
    return 0;
}